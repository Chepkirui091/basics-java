public class IfElseLoop {
    public static void main(String[] args) {

        //if statement
        {
            int x = 5;
            if (x < 10) {
                System.out.println("I am not interested");
            }
            {
                System.out.println("I am interested");
            }
        }

        //if else statement
        {
            boolean b = true;
            if (b == true) {
                System.out.println("Hello");
            }
            else {
                System.out.println("No Hello");
            }
        }
    }
}
